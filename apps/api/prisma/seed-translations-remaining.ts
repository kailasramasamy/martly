async function main() {
  console.log("Seeding remaining Tamil translations...\n");

  let catUpdated = 0;
  for (const [name, translations] of Object.entries(categoryTranslations)) {
    const result = await prisma.category.updateMany({
      where: { name },
      data: { translations: translations as unknown as Prisma.InputJsonValue },
    });
    if (result.count > 0) {
      catUpdated += result.count;
    } else {
      console.log(`  ⚠ Category not found: ${name}`);
    }
  }
  console.log(`Updated ${catUpdated} categories`);

  let prodUpdated = 0;
  for (const [name, translations] of Object.entries(productTranslations)) {
    const result = await prisma.product.updateMany({
      where: { name },
      data: { translations: translations as unknown as Prisma.InputJsonValue },
    });
    if (result.count > 0) {
      prodUpdated += result.count;
    } else {
      console.log(`  ⚠ Product not found: ${name}`);
    }
  }
  console.log(`Updated ${prodUpdated} products`);

  // Refresh search index
  console.log("\nRefreshing search index...");
  const translated = await prisma.product.findMany({
    where: { translations: { not: Prisma.DbNull } },
    select: { id: true },
  });
  for (const p of translated) {
    await prisma.$executeRawUnsafe(`UPDATE products SET name = name WHERE id = '${p.id}'`);
  }
  console.log(`Search index refreshed for ${translated.length} products.`);

  // Verify nothing remains untranslated
  const remainingCats = await prisma.category.count({ where: { translations: { equals: Prisma.DbNull } } });
  const remainingProds = await prisma.product.count({ where: { translations: { equals: Prisma.DbNull } } });
  console.log(`\nRemaining untranslated: ${remainingCats} categories, ${remainingProds} products`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
